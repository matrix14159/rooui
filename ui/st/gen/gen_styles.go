type styleItem struct {
	name   string
	writer styleWriter
}

type styleWriter func(name string) string

func simpleWriter(name string) string {
	return fmt.Sprintf(`
		// https://developer.mozilla.org/en-US/docs/Web/CSS/%s
		func %s(v any, u... string) *ui.Ref[ui.StyleItem] {
			return makeRefStyle("%s", v, u...)
		}`, name, styleName2FuncName(name), name)
}

func styleName2FuncName(name string) string {
	parts := strings.Split(name, "-")
	for i, one := range parts {
		parts[i] = strings.Title(one)
	}
	return strings.Join(parts, "")
}

func generateStyles() {
	f, err := os.Create("st/styles.go")
	if err != nil {
		panic(err)
	}
	defer f.Close()
	defer f.Sync()

	fmt.Fprintln(f, "package st")
	fmt.Fprintln(f)
	fmt.Fprintln(f, writeImports())
	fmt.Fprintln(f)
	fmt.Fprintln(f, "// this file is generated by go:generate, DO NOT EDIT.")

	for _, item := range items {
		writer := simpleWriter
		if item.writer != nil {
			writer = item.writer
		}
		fmt.Fprintln(f, writer(item.name))
	}
}

func writeImports() string {
	return fmt.Sprintf(`
		import (
			"github.com/matrix14159/rooui/ui"
		)`)
}

